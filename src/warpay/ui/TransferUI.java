    private boolean isTransferConfirmed() {
        int opt = JOptionPane.showConfirmDialog(null, "Are you sure want to tranfer the requested amount to " + fieldRecId.getText().trim() + "?", TYPE, JOptionPane.YES_NO_OPTION);
        return opt == JOptionPane.YES_OPTION;
    }

    private boolean isInputValid(String id, String amount, String pass) {
        if (id.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please enter recipient id");
            fieldRecId.requestFocusInWindow();
            return false;
        }

        if (amount.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please enter your tranfer amount");
            fieldAmount.requestFocusInWindow();
            return false;
        } else if (Double.valueOf(amount) == 0) {
            JOptionPane.showMessageDialog(null, "Please enter a valid transfer amount");
            fieldAmount.requestFocusInWindow();
            return false;
        }

        if (pass.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please enter your passcode");
            fieldPass.requestFocusInWindow();
            return false;
        }

        return true;
    }
