    private void buttonProcessActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonProcessActionPerformed
        String strRecId = fieldRecId.getText().trim();
        String strAmount = fieldAmount.getText().trim();
        String pass = String.valueOf(fieldPass.getPassword()).trim();

        if (!isInputValid(strRecId, strAmount, pass)) {
            return;
        }

        if (!Extension.isPassCorrect(pass)) {
            return;
        }

        if (!isTransferConfirmed()) {
            return;
        }

        double transferAmount = Double.valueOf(strAmount);
        int recipientId = Integer.valueOf(strRecId);
        double newBalance = Extension.reduceBalance(transferAmount);
        double recipientOldBalance;
        double recipientNewBalance;

        try {
            try (ResultSet res = Extension.getAccountBalance(recipientId)) {
                if (res.next()) {
                    recipientOldBalance = res.getDouble(1);
                    recipientNewBalance = Extension.addBalance(recipientOldBalance, transferAmount);
                } else {
                    JOptionPane.showMessageDialog(null, "Recipient account with id " + strRecId + " could not be found");
                    return;
                }
            }

            int res = Extension.insertTransactionDetails(TYPE, transferAmount);
            if (res == 1) {

                res = Extension.updateAccountBalance(newBalance);
                if (res == 1) {

                    res = Extension.updateAccountBalance(recipientId, recipientNewBalance);
                    if (res == 1) {
                        AccountData.getInstance().setBalance(newBalance);

                        JOptionPane.showMessageDialog(null, "Successfully transfer the requested amount to recipient account");

                        Extension.showAccountBalance(fieldBalance);
                        if (BalanceUI.getInstance() != null) {
                            BalanceUI.showBalance();
                        }
                        if (DashboardUI.getInstance() != null) {
                            DashboardUI.showTransactionHistory();
                        }

                        fieldAmount.setText("");
                        fieldPass.setText("");
                    }

                }
            }

        } catch (SQLException | ParseException e) {
            System.err.println(e.getMessage());
            JOptionPane.showMessageDialog(null,
                    e.getMessage(), "Error",
                    JOptionPane.INFORMATION_MESSAGE
            );
            System.exit(0);
        }
    }//GEN-LAST:event_buttonProcessActionPerformed

